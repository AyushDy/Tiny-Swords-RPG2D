using UnityEngine;

public class TransitionOneBehaviour : StateMachineBehaviour
{
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        Player_combat.instance.canReceiveInput = true;
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        var playerState = Player_combat.instance.playerState;
        if (Player_combat.instance.inputReceived)
        {
            if (playerState.TryStartAction(ActionState.Attacking))
            {
                animator.SetTrigger("AttackTwo");
                Player_combat.instance.InputManager2();
            }else{
                Debug.Log("Input received but action could not be started. Current Action: " + playerState.Action);
            }
            Player_combat.instance.inputReceived = false;
        }
    }

